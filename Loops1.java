package corejava;

public class Loops1 {

	public static void main(String[] args) {
		int i=0;
//		while loop
		while(i<5)
		{
			System.out.println("hi" +i);
			i++;
		}
		int j=0;
//		do while loop
		do
		{
			System.out.println("how are you" +j);
			j++;
		}while(j<5);
//		for loop
		for(int x=0;x<5;x++)
		{
			System.out.println(x);
		}
	}

}
